"""
헬스장 회원을 위한 알림 메시지 프롬프트 모음
"""
from langchain_core.prompts import ChatPromptTemplate

def get_praise_prompt():
    """
    출석률이 높은 회원을 위한 칭찬 프롬프트
    """
    template = """
    헬스장 회원 {name}님의 출석률이 높고, 개인 목표는 "{goal}"입니다.
    
    다음 요구사항을 엄격히 따라 메시지를 작성해주세요:
    1. 반드시 1줄 이내로 짧게 작성하세요.
    2. 출석률은 언급하지 말고, 회원의 노력을 칭찬하세요.
    3. 오직 개인 목표({goal})에만 관련된 내용만 포함하세요:
       - 건강 유지: 면역력, 건강한 생활습관 관련 칭찬
       - 체중 감량: 지방 연소, 달라진 체형에 대한 과감한 칭찬
       - 체형 관리: 멋진 몸매와 눈에 띄는 변화에 대한 칭찬
       - 정신적 건강 관리: 정신적 강인함, 긍정적 변화에 대한 칭찬
       - 신체 능력 강화: 향상된 근력과 체력에 대한 구체적 칭찬
       - 취미: 운동을 즐기는 열정과 헌신에 대한 칭찬
    4. 다른 목표에 대한 언급은 절대 포함하지 마세요.
    5. 직설적이고 강한 어조로 작성하세요.
    
    1줄 이내의 짧고 강한 칭찬 메시지만 반환하세요.
    """
    return ChatPromptTemplate.from_template(template)

def get_encouragement_prompt():
    """
    출석률이 보통인 회원을 위한 격려 프롬프트
    """
    template = """
    헬스장 회원 {name}님의 출석률이 보통이고, 개인 목표는 "{goal}"입니다.
    
    다음 요구사항을 엄격히 따라 메시지를 작성해주세요:
    1. 반드시 1줄 이내로 짧게 작성하세요.
    2. 출석률은 언급하지 말고, 진심 어린 독려와 더 열심히 운동하도록 동기부여하는 메시지를 포함하세요.
    3. 오직 개인 목표({goal})에만 관련된 긍정적인 조언 1개를 포함하세요:
       - 건강 유지: "조금 더 자주 오시면 면역력이 확실히 강화될 거예요"와 같은 독려 조언
       - 체중 감량: "운동 횟수를 늘리면 체중 감량 속도가 확실히 빨라질 거예요"와 같은 독려 조언
       - 체형 관리: "조금만 더 노력하면 눈에 띄는 변화가 생길 거예요"와 같은 독려 조언
       - 정신적 건강 관리: "더 자주 오시면 정신 건강 개선 효과가 훨씬 커질 거예요"와 같은 독려 조언
       - 신체 능력 강화: "꾸준히 오시면 근력이 더 빠르게 향상될 거예요"와 같은 독려 조언
       - 취미: "운동 횟수를 늘리면 더 큰 즐거움을 느끼실 거예요"와 같은 독려 조언
    4. 다른 목표에 대한 언급은 절대 포함하지 마세요.
    5. 메시지 끝에 긍정적인 동기부여 문구를 추가하세요.
    
    1줄 이내의 짧은 격려 메시지만 반환하세요.
    """
    return ChatPromptTemplate.from_template(template)

def get_motivation_prompt():
    """
    출석률이 낮은 회원을 위한 동기부여 프롬프트
    """
    template = """
    헬스장 회원 {name}님의 출석률이 낮고, 개인 목표는 "{goal}"입니다.
    
    다음 요구사항을 엄격히 따라 메시지를 작성해주세요:
    1. 반드시 1줄 이내로 짧게 작성하세요.
    2. 출석률은 언급하지 말고, 회원의 개인 목표({goal})에 맞는 재치있는 경각심 표현을 포함하세요. 너무 심하지 않게 적당한 수위로 작성하세요:
       - 건강 유지 예시: "이대로면 비타민보다 감기약을 더 자주 찾게 될지도?", "건강검진 결과가 점점 걱정되고 있지 않나요?"
       - 체중 감량 예시: "옷 사이즈가 점점 커지는 걸 느끼시나요?", "체중계 위에 올라가는 게 슬슬 두려워지진 않나요?"
       - 체형 관리 예시: "거울을 볼 때마다 한숨이 나오진 않으신가요?", "여름 해변 시즌이 조금 걱정되진 않으신가요?"
       - 정신적 건강 관리 예시: "요즘 작은 일에도 짜증이 늘었다면 신호일 수 있어요", "마음의 평화가 점점 멀어지고 있다고 느껴지진 않나요?"
       - 신체 능력 강화 예시: "계단 몇 개만 올라도 숨이 찬다면 문제가 있어요", "쇼핑백 들기가 점점 힘들어지진 않나요?"
       - 취미 예시: "운동복이 옷장에서 서럽게 기다리고 있을 것 같아요", "헬스장 회원권이 잠자고 있는 것 같은데 깨워주세요"
    3. 메시지 끝에 친근하고 긍정적인 동기부여 문구를 반드시 포함하세요.
    4. 다른 목표에 대한 언급은 절대 포함하지 마세요.
    5. 유머러스하면서도 공감대를 형성할 수 있게 작성하세요.
    
    1줄 이내의 짧은 동기부여 메시지만 반환하세요.
    """
    return ChatPromptTemplate.from_template(template) 