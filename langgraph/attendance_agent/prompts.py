def get_praise_prompt():
    """
    출석률이 높은 회원을 위한 칭찬 프롬프트
    """
    template = """
    헬스장 회원 {name}님의 출석률이 높고, 개인 목표는 "{personal_goal}"입니다.
    
    다음 요구사항을 엄격히 따라 메시지를 작성해주세요:
    1. 반드시 1줄 이내로 짧게 작성하세요.
    2. 출석률은 언급하지 말고, 회원의 노력을 칭찬하세요.
    3. 오직 개인 목표({personal_goal})에만 관련된 내용만 포함하세요:
       - 건강 유지: 면역력, 건강한 생활습관, 활력 증가, 더 많은 에너지, 건강한 몸 등에 대한 칭찬
       - 체중 감량: 지방 연소, 달라진 체형, 슬림해진 실루엣, 탄탄해진 바디라인, 옷핏 변화 등에 대한 칭찬
       - 체형 관리: 멋진 몸매, 눈에 띄는 변화, 탄력 있는 근육, 조화로운 비율, 자세 개선 등에 대한 칭찬
       - 정신적 건강 관리: 정신적 강인함, 스트레스 감소, 집중력 향상, 긍정적 마인드, 심리적 안정감 등에 대한 칭찬
       - 신체 능력 강화: 향상된 근력, 체력 증가, 지구력 개선, 운동 성과, 기능적 향상 등에 대한 칭찬
       - 취미: 운동을 즐기는 열정, 꾸준한 참여, 운동의 즐거움, 새로운 기술 습득, 취미로서의 성장 등에 대한 칭찬
    4. 이미 작성된 메시지와 다른 독창적인 표현을 사용하세요.
    5. 직설적이고 강한 어조로 작성하세요.
    6. 회원의 이름은 처음에 1번만 언급하고, 이후에는 반복하지 마세요.
    
    응답은 JSON 형식으로 message 필드에 짧은 메시지만 포함하세요.
    """
    return ChatPromptTemplate.from_template(template)

def get_encouragement_prompt():
    """
    출석률이 보통인 회원을 위한 격려 프롬프트
    """
    template = """
    헬스장 회원 {name}님의 출석률이 보통이고, 개인 목표는 "{personal_goal}"입니다.
    
    다음 요구사항을 엄격히 따라 메시지를 작성해주세요:
    1. 반드시 1줄 이내로 짧게 작성하세요.
    2. 출석률은 언급하지 말고, 진심 어린 독려와 더 열심히 운동하도록 동기부여하는 메시지를 포함하세요.
    3. 오직 개인 목표({personal_goal})에만 관련된 긍정적인 조언 1개를 포함하세요:
       - 건강 유지: 면역력 강화, 생활 에너지 증가, 만성질환 예방, 일상생활의 활력, 수면 개선 등에 관련된 조언
       - 체중 감량: 체중 감량 속도, 칼로리 소모, 체지방률 개선, 기초대사량 증가, 장기적인 체중 관리 등에 관련된 조언
       - 체형 관리: 눈에 띄는 변화, 근육량 증가, 체형 비율 개선, 특정 부위 강화, 바디라인 향상 등에 관련된 조언
       - 정신적 건강 관리: 정신 건강 개선, 스트레스 저하, 불안감 감소, 집중력 향상, 행복감 증가 등에 관련된 조언
       - 신체 능력 강화: 근력 향상, 지구력 증가, 유연성 개선, 균형감각 향상, 운동 수행능력 증가 등에 관련된 조언
       - 취미: 운동의 즐거움, 새로운 기술 습득, 성취감 경험, 운동 커뮤니티 참여, 취미로서의 성장 등에 관련된 조언
    4. 다른 목표에 대한 언급은 절대 포함하지 마세요.
    5. 메시지 끝에 다양한 긍정적인 동기부여 문구를 추가하세요.
    6. 이미 작성된 메시지와 다른 독창적인 표현을 사용하세요.
    7. 회원의 이름은 처음에 1번만 언급하고, 이후에는 반복하지 마세요.
    
    응답은 JSON 형식으로 message 필드에 짧은 메시지만 포함하세요.
    """
    return ChatPromptTemplate.from_template(template)

